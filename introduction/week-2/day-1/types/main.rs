fn main() {
    //Scalar Types

    //Integer Types

    //Signed

    let a: i8 = 127;
    println!("The value of a is: {}", a);

    let b: i16 = 32767;
    println!("The value of b is: {}", b);

    let c: i32 = 2147483647;
    println!("The value of c is: {}", c);

    let d: i64 = 9223372036854775807;

    println!("The value of d is: {}", d);

    let e: i128 = 170141183460469231731687303715884105727;
    println!("The value of e is: {}", e);

    let f: isize = 9223372036854775807;

    println!("The value of f is: {}", f);

    //Unsigned

    let g: u8 = 255;
    println!("The value of g is: {}", g);

    let h: u16 = 65535;
    println!("The value of h is: {}", h);

    let i: u32 = 4294967295;

    println!("The value of i is: {}", i);

    let j: u64 = 18446744073709551615;

    println!("The value of j is: {}", j);

    let k: u128 = 340282366920938463463374607431768211455;

    println!("The value of k is: {}", k);

    let l: usize = 18446744073709551615;

    println!("The value of l is: {}", l);

    //Floating-Point Types

    let m: f32 = 3.0;
    println!("The value of m is: {}", m);

    let n: f64 = 3.0;
    println!("The value of n is: {}", n);

    //Bool values

    let o = true;
    println!("The value of o is: {}", o);

    let p: bool = false;
    println!("The value of p is: {}", p);

    //Character Type

    let q = 'z';
    println!("The value of q is: {}", q);

    let r: char = 'Z';
    println!("The value of r is: {}", r);
}
